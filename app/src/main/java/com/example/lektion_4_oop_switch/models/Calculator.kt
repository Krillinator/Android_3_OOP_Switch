package com.example.lektion_4_oop_switch.models

class Calculator {

    // Colon, after parenthesis = return TYPE
    fun addition(x: Int, y: Int): Int {

        return x + y
    }

    fun subtraction(x: Int, y: Int): Int {
        return x - y
    }

    fun multiplication(x: Int, y: Int): Int {
        return x * y
    }

    fun division(x: Int, y: Int): Int {
        return x / y
    }

}